package model;

/**
 * Enum that contains the the possible types of a GameObject.
 */
public enum GameObjectType {
    /**
     * Type of the enemy ships.
     * */
    EnemyShip,
    /**
     * Type of the player ship.
     * */
    PlayerShip,
    /**
     * Type of torpedo shot by enemy ships.
     * */
    EnemyTorpedo,
    /**
     * Type of torpedo shot by the player.
     * */
    PlayerTorpedo
}
